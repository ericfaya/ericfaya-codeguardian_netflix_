package com.nttdata.indhub.controller.impl;

public class Hola {
    private String name;
    private int id;

    public Hola() {

    }
    public Hola(String name, int id) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

}
